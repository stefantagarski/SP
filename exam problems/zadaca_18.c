//За квадратна матрица А со димензии n x n, од стандарден влез се внесува бројот n (n > 2) и елементите
// на матрицата (реални броеви). Нека X e збирот од елементите под главната дијагонала во матрицата А.
// Нека Y е збирот од елементите под споредната дијагонала во матрицата А. Да се креира нова матрица B на следниот начин:
//сите елементи од главната дијагонала во матрицата B треба да имаат вредност X
//сите елементи од споредната дијагонала во матрицата B треба да имаат вредност Y
//ако даден елемент припаѓа и на главната и на споредната дијагонала во матрицата B, тогаш неговата вредност e X+Y
//сите останати елементи во матрицата B имаат вредност 0

#include <stdio.h>

int main() {
    int n;
    scanf("%d", &n);
    double matrix[n][n];
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            scanf("%lf", &matrix[i][j]);
        }
    }
    double x = 0;
    double y = 0;
    for (int i = 0; i < n; i++) {
    for (int j = 0; j < i; j++) {
        x += matrix[i][j];
        y += matrix[i][n-1-j];
        }
    }
     double mtr[100][100];
     for(int i = 0;i < n;i++){
         for(int j = 0;j < n;j++){
            if(i == j && i == n-1-j){
                mtr[i][j] = x + y;
            }
           else  if(i == j){
                mtr[i][j] = x;
            }
              else if(i+j == n-1){
                 mtr[i][j] = y;
             }

             else {
                 mtr[i][j] = 0;
             }
             printf("%.1lf  ",mtr[i][j]);
         }
         printf("\n");
     }

    return 0;
}
